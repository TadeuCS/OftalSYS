    private void salvar(String cor) {
        try {
            Cor c = new Cor();
            corDAO = new CorDAO();
            c.setDescricao(cor);
            corDAO.salvar(c);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar a cor!\n"+e.getMessage());
        }
    }
